def sandwich_request(ingredients, order) 
  order_build = Array.new
  order.each do |order_item|
    ingredients.each do |ingredient_set|
      if ingredient_set.include?(order_item)
          order_build.push order_item
      end
    end
  end
  order_build == order 
end

